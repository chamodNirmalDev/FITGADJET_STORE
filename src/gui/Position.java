    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String position = jTextField1.getText();

        if (position.isEmpty()) {
            Notifications.getInstance().show(Notifications.Type.INFO, Notifications.Location.TOP_CENTER, "Please Enter New Position");
        } else {
            try {
                ResultSet resultSet = MySQL2.executeSearch("SELECT * FROM `employee_type` WHERE `type` = '" + position + "'");

                if (resultSet.next()) {
                    Notifications.getInstance().show(Notifications.Type.INFO, Notifications.Location.TOP_CENTER, "This Position Is Already Added!");
                } else {
                    MySQL2.executeIUD("INSERT INTO `employee_type` (`type`) VALUES ('" + position + "')");
                    Notifications.getInstance().show(Notifications.Type.SUCCESS, Notifications.Location.TOP_CENTER, "Position Added Successfully!");
                }
                loadPosition();
            } catch (Exception e) {
                logger.log(Level.WARNING, "Exception In position in add button", e);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String position = jTextField1.getText();
        int selectedRow = jTable1.getSelectedRow();

        if (position.isEmpty()) {
            Notifications.getInstance().show(Notifications.Type.INFO, Notifications.Location.TOP_CENTER, "Please Enter New Position");
        } else if (selectedRow == -1) {
            Notifications.getInstance().show(Notifications.Type.INFO, Notifications.Location.TOP_CENTER, "Please Select a Position to Update");
        } else {
            try {
                // Assuming the ID is in the first column (index 0)
                Object id = jTable1.getModel().getValueAt(selectedRow, 0);

                ResultSet resultSet = MySQL2.executeSearch("SELECT * FROM `employee_type` WHERE `id` = '" + id + "'");
                if (!resultSet.next()) {
                    Notifications.getInstance().show(Notifications.Type.INFO, Notifications.Location.TOP_CENTER, "Position Not Found in Database!");
                } else {
                    MySQL2.executeIUD("UPDATE `employee_type` SET `type` = '" + position + "' WHERE `id` = '" + id + "'");
                    Notifications.getInstance().show(Notifications.Type.SUCCESS, Notifications.Location.TOP_CENTER, "Position Updated Successfully!");
                }

                loadPosition(); // Reload the table data
            } catch (Exception e) {
                logger.log(Level.WARNING, "Exception in position update button", e);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

