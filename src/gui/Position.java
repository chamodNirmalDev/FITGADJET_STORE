    private void loadPosition() {
        try {
            ResultSet resultSet = MySQL2.executeSearch("SELECT * FROM `employee_type` ORDER BY `id` ASC ");

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {
                Vector<String> vector = new Vector<>();
                vector.add(resultSet.getString("id"));
                vector.add(resultSet.getString("type"));
                model.addRow(vector);

            }
            reset();
        } catch (Exception e) {
//            e.printStackTrace();
            logger.log(Level.WARNING, "Exception In position in loadPosition", e);
        }
    }

    private void reset() {
        jTextField1.setText("");
        jTable1.clearSelection();
        jButton1.setEnabled(true);
    }
