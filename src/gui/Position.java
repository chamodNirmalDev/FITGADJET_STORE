    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String position = jTextField1.getText();

        if (position.isEmpty()) {
           Notifications.getInstance().show(Notifications.Type.INFO, Notifications.Location.TOP_CENTER, "Please Enter New Position");
        } else {
            try {

                ResultSet resultSet = MySQL2.executeSearch("SELECT COUNT(*) AS count FROM `employee` WHERE `employee_type_id` = (SELECT id FROM `employee_type` WHERE `type` = '" + position + "')");

                if (resultSet.next() && resultSet.getInt("count") > 0) {
                    Notifications.getInstance().show(Notifications.Type.INFO, Notifications.Location.TOP_CENTER, "Cannot delete Position because it is associated with Employee.");
                } else {
                    MySQL2.executeIUD("DELETE FROM `employee_type` WHERE `type` = '" + position + "'");
                    Notifications.getInstance().show(Notifications.Type.SUCCESS, Notifications.Location.TOP_CENTER, "Position Deleted Successfully!");
                    loadPosition();
                    reset();
                }
            } catch (Exception e) {
                logger.log(Level.WARNING, "Exception In position in delete button", e);
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed


