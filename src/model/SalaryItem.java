package model;

public class SalaryItem {

    private String date;
    private String userID;
    private String name;
    private String position;
    private String workdays;
    private String OT;
    private String OTRate;
    private String transportAllowence;
    private String foodAllowence;
    private String epf;
    private String etf;
    private String salary;

    /**
     * @return the date
     */
    public String getDate() {
        return date;
    }

    /**
     * @param date the date to set
     */
    public void setDate(String date) {
        this.date = date;
    }

    /**
     * @return the userID
     */
    public String getUserID() {
        return userID;
    }

    /**
     * @param userID the userID to set
     */
    public void setUserID(String userID) {
        this.userID = userID;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the workdays
     */
    public String getWorkdays() {
        return workdays;
    }

    /**
     * @param workdays the workdays to set
     */
    public void setWorkdays(String workdays) {
        this.workdays = workdays;
    }

    /**
     * @return the OT
     */
    public String getOT() {
        return OT;
    }

    /**
     * @param OT the OT to set
     */
    public void setOT(String OT) {
        this.OT = OT;
    }

    /**
     * @return the OTRate
     */
    public String getOTRate() {
        return OTRate;
    }

    /**
     * @param OTRate the OTRate to set
     */
    public void setOTRate(String OTRate) {
        this.OTRate = OTRate;
    }

    /**
     * @return the transportAllowence
     */
    public String getTransportAllowence() {
        return transportAllowence;
    }

    /**
     * @param transportAllowence the transportAllowence to set
     */
    public void setTransportAllowence(String transportAllowence) {
        this.transportAllowence = transportAllowence;
    }

    /**
     * @return the foodAllowence
     */
    public String getFoodAllowence() {
        return foodAllowence;
    }

    /**
     * @param foodAllowence the foodAllowence to set
     */
    public void setFoodAllowence(String foodAllowence) {
        this.foodAllowence = foodAllowence;
    }

    /**
     * @return the epf
     */
    public String getEpf() {
        return epf;
    }

    /**
     * @param epf the epf to set
     */
    public void setEpf(String epf) {
        this.epf = epf;
    }

    /**
     * @return the etf
     */
    public String getEtf() {
        return etf;
    }

    /**
     * @param etf the etf to set
     */
    public void setEtf(String etf) {
        this.etf = etf;
    }

    /**
     * @return the salary
     */
    public String getSalary() {
        return salary;
    }

    /**
     * @param salary the salary to set
     */
    public void setSalary(String salary) {
        this.salary = salary;
    }

    /**
     * @return the position
     */
    public String getPosition() {
        return position;
    }

    /**
     * @param position the position to set
     */
    public void setPosition(String position) {
        this.position = position;
    }

}
